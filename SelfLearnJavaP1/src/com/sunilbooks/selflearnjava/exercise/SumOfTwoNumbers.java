package com.sunilbooks.selflearnjava.exercise;

/**
 * Calculates and prints the sum of two integers.
 * 
 * This simple program demonstrates the basic addition operation by calculating 
 * the sum of two hardcoded integers and printing the result to the console.
 * 
 * @version 1.0
 * @since 16 Nov 2014
 * @author Sunil Sahu
 * @Copyright (c) Sunil Sahu
 * @url www.sunilbooks.com
 */
public class SumOfTwoNumbers {

    /**
     * The entry point of the application. Initializes two integers, calculates 
     * their sum, and prints the result to the console.
     * 
     * @param args Command line arguments (not used here).
     */
    public static void main(String[] args) {
        int a = 1; // First integer
        int b = 2; // Second integer
        int sum = a + b; // Sum of the two integers
        System.out.println("Sum is " + sum); // Output the result
    }

}
